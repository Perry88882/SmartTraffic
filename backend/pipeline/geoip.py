def lookup(ip_str: str) -> dict:
    """
    查询 IP 的物理位置。

    Returns:
        {
            country: "中国" | "美国" | ...
            region:  "北京" | "广东" | ... (国内精确到省市)
            city:    "北京" | "上海" | ...
            lat/lng: 坐标
            continent: "Asia" | "Europe" | ...
        }
    """
    result = {"country": "未知", "region": "", "city": "", "lat": 0, "lng": 0, "continent": ""}
    if not ip_str:
        return result

    try:
        ip = ipaddress.ip_address(ip_str)
    except ValueError:
        return result

    # 本地/内网/环回
    if ip.is_private or ip.is_loopback:
        result["country"] = "本地"
        result["region"] = "局域网"
        result["city"] = "内网"
        return result
    if ip.is_multicast:
        result["country"] = "多播"
        return result

    # 1. 先匹配中国城市级
    for nets, region_name, lat, lng in CN_REGIONS:
        for net_str in nets:
            try:
                if ip in ipaddress.ip_network(net_str):
                    result["country"] = "中国"
                    result["region"] = region_name
                    result["city"] = region_name
                    result["lat"] = lat
                    result["lng"] = lng
                    result["continent"] = "Asia"
                    return result
            except ValueError:
                continue

    # 2. 匹配国际
    for net_str, country, continent in INTL_REGIONS:
        try:
            if ip in ipaddress.ip_network(net_str):
                result["country"] = country
                result["region"] = country
                result["city"] = country
                result["continent"] = continent
                return result
        except ValueError:
            continue

    result["country"] = "未知"
    return result


